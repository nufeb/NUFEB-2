CONST=0.8D0

D_O2=DIF_COEF_O2
D_NO2=DIF_COEF_NO2
D_NO3=DIF_COEF_NO3
D_NH4=DIF_COEF_NH4
D_S=DIF_COEF_S    


DO I=1,MX
    DO J=1,MY
        DO K=1,MZ
			
            !VERSION2 IMPROVEMENTS
            IF (BIOMASS_OCCUPANCY(I,J,K)==1) THEN   !DIFFUSION COEF WITHIN BIOMASS IS ABOUT 80% (iDynoMiCS) 
                D_O2(I,J,K)=DIF_COEF_O2*CONST
