DOUBLE PRECISION FUNCTION GS(S,I,J,K) !GHOST POINT VALUES OF SUBSTRATE
USE GLOBAL_VARIABLES, ONLY:MX,MY,MZ
USE BCND
IMPLICIT NONE
INTEGER::I,J,K
REAL(8)::S(MX,MY,MZ)
IF (I==1) THEN
    IF (XBCS==0) THEN
        GS=S(MX,J,K)
    END IF
	IF (XBCS==1) THEN
        GS=2*XBNS(J,K)-S(I,J,K)
    END IF
    
    IF (XBCS==2) THEN
        GS=S(I,J,K)
    END IF
    

ELSE IF (I==MX) THEN  
	IF (XBCS==0) THEN
        GS=S(1,J,K)
    END IF
    IF (XBCS==1) THEN
        GS=2*XBPS(J,K)-S(I,J,K)
    END IF
    
    IF (XBCS==2) THEN
        GS=S(I,J,K)
    END IF
    
ELSE IF (J==1) THEN
	IF (YBCS==0) THEN 
        GS=S(I,MY,K)
    END IF
    IF (YBCS==1) THEN 
        GS=2*YBNS(I,K)-S(I,J,K)
    END IF
    IF (YBCS==2) THEN
        GS=S(I,J,K)
    END IF
    
ELSE IF (J==MY) THEN 
	IF (YBCS==0) THEN 
        GS=S(I,1,K)
    END IF
    IF (YBCS==1) THEN
        GS=2*YBPS(I,K)-S(I,J,K) 
    END IF
    IF (YBCS==2) THEN
        GS=S(I,J,K)
    END IF
ELSE IF (K==1) THEN
	IF (ZBCS==0) THEN
        GS=S(I,J,MZ)
    END IF
    IF (ZBCS==1) THEN
        GS=2*ZBNS(I,J)-S(I,J,K)
    END IF
    IF (ZBCS==2) THEN
        GS=S(I,J,K)
    END IF
ELSE IF (K==MZ) THEN 
	IF (ZBCS==0) THEN
        GS=S(I,J,1)
    END IF
    IF (ZBCS==1) THEN
        GS=2*ZBPS(I,J)-S(I,J,K) 
    END IF
    IF (ZBCS==2) THEN
        GS=S(I,J,K)
    END IF
END IF
       
END FUNCTION GS 

