BIOMASS_OCCUPANCY=0

DO I=1,BAC_N

	II=FLOOR(BAC_X(I)/DX)+1
	JJ=FLOOR(BAC_Y(I)/DY)+1
	KK=FLOOR(BAC_Z(I)/DZ)+1
   
    IF (ABS(II)>MX .OR. ABS(JJ)>MY .OR. ABS(KK)>MZ) THEN
        PRINT*,'ERROR IN BIOMASS ON GRID',I,II,JJ,KK, BAC_S(I),BAC_X(I)
        PAUSE
    END IF
    
